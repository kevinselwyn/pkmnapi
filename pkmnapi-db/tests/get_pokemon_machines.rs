use pkmnapi_db::*;

mod common;

macro_rules! get_pokemon_machines_test {
    ($test_name:ident, $pokedex_id:expr, $pokemon_machines:expr) => {
        #[test]
        #[ignore]
        #[allow(non_snake_case)]
        fn $test_name() {
            let db = common::load_rom();

            match db.get_pokemon_machines(&$pokedex_id) {
                Ok(pokemon_machines) => assert_eq!(
                    pokemon_machines, $pokemon_machines,
                    "Searched for Pokédex ID: {}",
                    $pokedex_id
                ),
                Err(_) => panic!(format!("Could not find Pokédex ID: {}", $pokedex_id)),
            };
        }
    };
}

