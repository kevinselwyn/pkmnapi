mod common;

macro_rules! set_pokemon_stats_test {
    (
        $test_name:ident,
        $pokedex_id:expr,
        $base_hp:expr,
        $base_attack:expr,
        $base_defence:expr,
        $base_speed:expr,
        $base_special:expr,
        $type_ids:expr,
        $catch_rate:expr,
        $base_exp_yield:expr,
        $growth_rate:expr,
        $patch_offset:expr,
        $patch_data:expr
    ) => {
        #[test]
        #[ignore]
        #[allow(non_snake_case)]
        fn $test_name() {
            let db = common::load_rom();

            match db.set_pokemon_stats(
                &$pokedex_id,
                &PokemonStats {
                    pokedex_id: $pokedex_id,
                    base_hp: $base_hp,
                    base_attack: $base_attack,
                    base_defence: $base_defence,
                    base_speed: $base_speed,
                    base_special: $base_special,
                    type_ids: $type_ids,
                    catch_rate: $catch_rate,
                    base_exp_yield: $base_exp_yield,
                    growth_rate: $growth_rate,
                },
            ) {
                Ok(patch) => assert_eq!(
                    patch,
                    Patch {
                        offset: $patch_offset,
                        length: $patch_data.len(),
                        data: $patch_data
                    },
                    "Searched for Pokédex ID: {}",
                    $pokedex_id
                ),
                Err(_) => panic!(format!("Could not find Pokédex ID: {}", $pokedex_id)),
            };
        }
    };
}

