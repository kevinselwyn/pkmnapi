use pkmnapi_db::patch::*;
use pkmnapi_db::string::*;
use pkmnapi_db::types::*;

mod common;

macro_rules! set_item_name_test {
    ($test_name: ident, $item_id: expr, $item_name: expr, $patch_offset: expr, $patch_data: expr) => {
        #[test]
        #[ignore]
        #[allow(non_snake_case)]
        fn $test_name() {
            let db = common::load_rom();

            match db.set_item_name(
                &$item_id,
                &ItemName {
                    name: ROMString::from($item_name),
                },
            ) {
                Ok(patch) => assert_eq!(
                    patch,
                    Patch {
                        offset: $patch_offset,
                        length: $patch_data.len(),
                        data: $patch_data
                    },
                    "Searched for item ID: {}",
                    $item_id
                ),
                Err(_) => panic!(format!("Could not find item ID: {}", $item_id)),
            };
        }
    };
}

