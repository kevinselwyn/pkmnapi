use pkmnapi_db::cry::*;
use pkmnapi_db::patch::*;

mod common;

macro_rules! set_pokemon_cry_test {
    (
        $test_name:ident,
        $pokedex_id:expr,
        $base:expr,
        $pitch:expr,
        $length:expr,
        $patch_offset:expr,
        $patch_data:expr
    ) => {
        #[test]
        #[ignore]
        #[allow(non_snake_case)]
        fn $test_name() {
            let db = common::load_rom();

            match db.set_pokemon_cry(
                &$pokedex_id,
                &Cry {
                    base: $base,
                    pitch: $pitch,
                    length: $length,
                    ..Default::default()
                },
            ) {
                Ok(patch) => assert_eq!(
                    patch,
                    Patch {
                        offset: $patch_offset,
                        length: $patch_data.len(),
                        data: $patch_data
                    },
                    "Searched for Pokédex ID: {}",
                    $pokedex_id
                ),
                Err(_) => panic!(format!("Could not find Pokédex ID: {}", $pokedex_id)),
            };
        }
    };
}

