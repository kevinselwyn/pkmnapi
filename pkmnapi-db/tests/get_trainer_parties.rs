use pkmnapi_db::types::*;

mod common;

macro_rules! get_trainer_parties_test {
    ($test_name: ident, $trainer_id: expr, $trainer_parties: expr) => {
        #[test]
        #[ignore]
        #[allow(non_snake_case)]
        fn $test_name() {
            let db = common::load_rom();

            match db.get_trainer_parties(&$trainer_id) {
                Ok(trainer_parties) => assert_eq!(
                    trainer_parties,
                    $trainer_parties
                        .iter()
                        .map(|trainer_party| {
                            let pokemon = trainer_party
                                .iter()
                                .map(|(level, pokedex_id)| PartyPokemon::new(*level, *pokedex_id))
                                .collect::<Vec<PartyPokemon>>();

                            Party::new(&pokemon)
                        })
                        .collect::<Vec<Party>>(),
                    "Searched for trainer ID: {}",
                    $trainer_id
                ),
                Err(_) => panic!(format!("Could not find trainer ID: {}", $trainer_id)),
            };
        }
    };
}

