use pkmnapi_db::patch::*;

mod common;

#[test]
#[ignore]
#[allow(non_snake_case)]
fn set_pokemon_title() {
    let db = common::load_rom();

    let patch = db
        .set_pokemon_title(&vec![
            0x85, 0x85, 0x85, 0x85, 0x85, 0x85, 0x85, 0x85, 0x85, 0x85, 0x85, 0x85, 0x85, 0x85,
            0x85, 0x85,
        ])
        .unwrap();

    assert_eq!(
        patch,
        Patch {
            offset: 0x4588,
            length: 0x10,
            data: vec![
                0x85, 0x85, 0x85, 0x85, 0x85, 0x85, 0x85, 0x85, 0x85, 0x85, 0x85, 0x85, 0x85, 0x85,
                0x85, 0x85
            ]
        }
    );
}
