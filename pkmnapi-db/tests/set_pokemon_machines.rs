use pkmnapi_db::patch::*;
use pkmnapi_db::*;

mod common;

macro_rules! set_pokemon_machines_test {
    (
        $test_name:ident,
        $pokedex_id:expr,
        $pokemon_machines:expr,
        $patch_offset:expr,
        $patch_data:expr
    ) => {
        #[test]
        #[ignore]
        #[allow(non_snake_case)]
        fn $test_name() {
            let db = common::load_rom();

            match db.set_pokemon_machines(&$pokedex_id, &$pokemon_machines) {
                Ok(patch) => assert_eq!(
                    patch,
                    Patch {
                        offset: $patch_offset,
                        length: $patch_data.len(),
                        data: $patch_data
                    },
                    "Searched for Pokédex ID: {}",
                    $pokedex_id
                ),
                Err(_) => panic!(format!("Could not find Pokédex ID: {}", $pokedex_id)),
            };
        }
    };
}

