mod common;

#[test]
#[ignore]
#[allow(non_snake_case)]
fn get_pokemon_title() {
    let db = common::load_rom();

    let pokemon_title = db.get_pokemon_title().unwrap();

    #[cfg(feature = "PKMN_RED")]
    assert_eq!(
        pokemon_title,
        vec![
            0xB0, 0xB1, 0x99, 0x70, 0x03, 0x1A, 0x54, 0x04, 0x01, 0x94, 0x19, 0x4C, 0x96, 0x22,
            0xA3, 0x85,
        ]
    );

    #[cfg(not(feature = "PKMN_RED"))]
    assert_eq!(
        pokemon_title,
        vec![
            0xB1, 0xB0, 0x99, 0x39, 0x2B, 0x52, 0x28, 0xAB, 0x68, 0x84, 0xBA, 0x47, 0x46, 0xAA,
            0x0E, 0x55
        ]
    );
}
