use pkmnapi_db::patch::*;
use pkmnapi_db::pic::*;
use std::fs;

mod common;

macro_rules! set_trainer_pic_test {
    (
        $test_name:ident,
        $trainer_id:expr,
        $pic_filename:expr,
        $encoding_method:expr,
        $patch_offset:expr,
        $patch_data:expr
    ) => {
        #[test]
        #[ignore]
        #[allow(non_snake_case)]
        fn $test_name() {
            let db = common::load_rom();
            let pic_data = fs::read($pic_filename).unwrap();

            let pic = Pic::from_png(pic_data).unwrap();

            match db.set_trainer_pic(&$trainer_id, &pic, $encoding_method) {
                Ok(patch) => assert_eq!(
                    patch,
                    Patch {
                        offset: $patch_offset,
                        length: $patch_data.len(),
                        data: $patch_data
                    },
                    "Searched for trainer ID: {}",
                    $trainer_id
                ),
                Err(_) => panic!(format!("Could not find trainer ID: {}", $trainer_id)),
            };
        }
    };
}

